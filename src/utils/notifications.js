import { toast } from 'react-toastify';
import 'react-toastify/dist/ReactToastify.css';

export const showAsyncErrorNotification = message => {
  setTimeout(() => {
    toast.error(message, {
      position: 'top-right',
      autoClose: 3000,
      hideProgressBar: false,
      closeOnClick: true,
      pauseOnHover: true,
      draggable: true,
      progress: undefined,
    });
  }, 0);
};

